public class arithmatic {
    public static void main(String[] args) {
        int a = 5;
        int b = 9;
        int c = 10;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a % c);
        System.out.println(a / b);
        System.out.println(5.0 / 9);
        System.out.println(9 / 10);
        System.out.println(9.0 / 5);

    }
}
