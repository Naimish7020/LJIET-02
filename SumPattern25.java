import java.util.*;

public class SumPattern25 {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int i = 1;
        int sum = 0;
        while (i <= n) {

            System.out.print();
            System.out.print("=");
            sum=sum+
            System.out.print(sum);
        }
        System.out.println();
        i++;
    }
}
